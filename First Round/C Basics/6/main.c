#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x,y;
    printf("Enter The First Number here : ");
    scanf("%d",&x);
    printf("Enter The Second Number here : ");
    scanf("%d",&y);
    printf("x+y=%d\n",x+y);
    printf("x-y=%d\n",x-y);
    printf("x*y=%d\n",x*y);
    printf("x/y=%d\n",x/y);
    printf("x&y=%d\n",x&y);
    printf("x|y=%d\n",x|y);
    printf("x^y=%d\n",x^y);
    return 0;
}
